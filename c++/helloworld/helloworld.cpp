#include <iostream> // header file library that lets us work with input and output objects such as cout. Adds functionality to C++ programs.
using namespace std; //We can use names for objects and variables from the standard library.

int main(){ //function. Any code inside the curly brackets will be executed. 
    cout << "Hello World.\t"; //object. (<<) is an insertion operator to output/print text.
    cout << "I am learning \"C++\"";
    return 0; //ends the main function. Every C++ statement ends with a semicolon (;).
} //ends the main function.

// \t : creates a horizontal tab
// \\ : inserts a blackslash character (\)
// \" : inserts a double quote character
// // : comments
// /* : start a multi-line comment